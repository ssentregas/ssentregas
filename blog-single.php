                        <h2 class="hentry__title"><a href="blog-single.php">Ways to help your children develop self-esteem and confidence</a></h2>
                        <a href="blog-single.php"> <img width="850" height="480" src="assets/images/image-28.jpg" class="img-responsive wp-post-image" alt="MentalPress 30" srcset="assets/images/image-28-300x169.jpg 300w, assets/images/image-28.jpg 850w" sizes="(max-width: 850px) 100vw, 850px"> </a>

                        <div class="hentry__content">
                            <p>Self-esteem and confidence are major traits in individuals that affect their success. While these are a lifelong process, the foundation of it needs to be established in early childhood. Building self-esteem will allow the child to deal with difficult situations that they will encounter during their lifetime. Since parents have the greatest influence on a child’s belief, it is important for them to let their child know where they belong, how well they are doing and contribute towards developing confidence and self-esteem.</p>
                            <p><span id="more-216"></span></p>
                            <p>&nbsp;</p>
                            <p>Here are some practical tips that will help you encourage self-esteem and confidence in your child without trying hard:</p>
                            <ul>
                                <li><strong>Step back</strong> – While you may be worried about your child failing or not doing good on a task, it is best to take a step back and not help them at every step. You need to be able to help your child become competent and not be dependable on others. Allow your child to take healthy risks and solve problems on their own. Once they are able to accomplished them, recognize their success.</li>
                                <li><strong>Praise, don’t over praise</strong> – Praising is a issue and there is a very thin line between praising and overpraising. While complements every now and then are important for developing self-esteem, overdoing it will only lower the bar and they will no longer push themselves to get better results. So, limit compliments to major achievements and where they are deemed important.</li>
                            </ul>
                            <ul>
                                <li><strong>Assign small tasks around the house</strong> – Allow your children to help you around the house by giving them small tasks. This will encourage them to showcase their skills and also make them realize they are making a contribution and will feel appreciated by other family members.</li>
                            </ul>
                            <ul>
                                <li><strong>Allow them to pursue new interests</strong> – Encourage your children to take up new interests and pursue them. Set achievable goals on their particular interests so that when they achieve them, it will give them a sense of accomplishment that will boost their self-esteem.</li>
                            </ul>
                            <p>You need to use your influence in the correct manner in the early lives your children, so that they grow into responsible, competent and confident adults.</p>
